const dictionaries: Record<string, Dictionary> = {
    en,
    es
};

/**
 * useTranslation returns a translation function (t) that reacts to the global `app:language` setting natively.
 */
export function useTranslation() {
    const langSetting = useAtomValue(getSettingsKeyAtom("app:language"));
    const currentLang = (langSetting === "es" ? "es" : "en"); // fallback to en
    const dict = dictionaries[currentLang] || en;

    return {
        t: (key: string, _fallback?: string): string => {
            return dict[key] || _fallback || key;
        },
        currentLang
    };
}
